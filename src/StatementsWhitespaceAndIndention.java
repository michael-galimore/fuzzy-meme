public class StatementsWhitespaceAndIndention {

    public static void main(String[] args) {
        int myVariable = 50;
        myVariable++;
        myVariable--;
        System.out.println("This is a test");

        System.out.println("This is" +
        " another"
        + " statement");

//       These are all statements. Java doesn't mind whitespaces. Indenting helps make code more readable.
    }
}
